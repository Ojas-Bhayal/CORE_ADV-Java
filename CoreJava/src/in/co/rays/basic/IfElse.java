package in.co.rays.basic;

public class IfElse {
	public static void main(String[] args) {
		int money = 99;
		if (money > 100) {
			System.out.println("Wow! I can buy a pizza :)");
		} else if (money > 90) {
			System.out.println("I can buy burger :)");
		} else {
			System.out.println("Oh! I cannot buy a pizza :(");
		}
	}
}
