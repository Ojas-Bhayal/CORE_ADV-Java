package in.co.rays.basic;

public class Squence {
	public static void main(String[] args) {
		// Printing Sequence of Number

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("Reverse Order : ");
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
	}
}
